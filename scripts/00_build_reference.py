def main() -> None:
    """Generate reference emergencies dataset."""
    print("[00] Building reference emergencies dataset...")
    OUTPUT_DIR.mkdir(parents=True, exist_ok=True)

    output = {
        "generated_at": datetime.now(timezone.utc).isoformat(),
        "description": (
            "News-sourced reference dataset of known active states of emergency. "
            "Each entry links to a verifiable source (news article, government press "
            "release, or humanitarian tracker). Updated by the pipeline and supplemented "
            "by live GDELT/ReliefWeb feeds."
        ),
        "entry_count": len(REFERENCE_EMERGENCIES),
        "emergencies": REFERENCE_EMERGENCIES,
    }

    out_path = OUTPUT_DIR / "reference_emergencies.json"
    out_path.write_text(
        json.dumps(output, indent=2, ensure_ascii=False),
        encoding="utf-8",
    )
    print(f"  → {len(REFERENCE_EMERGENCIES)} reference emergencies → {out_path.name}")


if __name__ == "__main__":
    main()
